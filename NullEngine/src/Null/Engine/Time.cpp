namespace NULLENGINE
{
	double m_previousTime;
	float m_DeltaTime;

	void Time::Update()
	{
		double currentTime = glfwGetTime();
		m_deltaTime = static_cast<float>(currentTime - m_previousTime);
		m_previousTime = currentTime;
		m_deltaTime = std::clamp(m_deltaTime, 0.0f, 0.05f);
	}
}
